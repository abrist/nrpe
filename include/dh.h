#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xDD,0x3F,0xC2,0x7E,0x4F,0xC4,0xA6,0x85,0x84,0x72,0x87,0x65,
		0x38,0x4C,0xEB,0x83,0x0D,0x47,0x0F,0xF7,0x38,0x2D,0x7C,0x75,
		0x0A,0x1C,0xD6,0x84,0x8B,0xFC,0x45,0x9E,0x80,0x2E,0x83,0x78,
		0xA5,0x27,0x07,0x89,0x87,0x76,0xA1,0xA7,0x1D,0x27,0xF8,0x9E,
		0xB1,0xAC,0xD7,0xB7,0x47,0x75,0xC0,0x68,0xB4,0x86,0xFA,0xA0,
		0x92,0xED,0x99,0x43,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
